
package com.syntax.reviewclass4;

public class Nestedloops {

	public static void main(String[] args) {
		for(int a=7;a>1;a--){
  for(int b=1;b<=a;b++){
    System.out.print(b+" ");
  }
System.out.println();


}
for(int i=1;i<=7;i++) {
  for(int j=1;j<=i;j++){
    System.out.print(j+" ");
  }
System.out.println();

}
 }
}
